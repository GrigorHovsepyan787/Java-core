package homework.homework1;

public class Comparingif {
    public static void main(String[] args) {
        int x = 10;
        int y = 20;
        if (x > y) {
            System.out.println("x > y.");
        }
        if (x < y) {
            System.out.println("x < y.");
        }
        if (x == y) {
            System.out.println("x = y.");
        }
    }
}
